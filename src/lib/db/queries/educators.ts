// ─── Submission work photos (educator-work bucket) ──────────────────────

const EXT_BY_MIME: Record<string, string> = {
  "image/jpeg": "jpg",
  "image/png": "png",
  "image/webp": "webp",
};

export interface SubmissionImageUpload {
  mediaType: string;
  /** base64 (no data: prefix) */
  data: string;
}

/** Store a student's work photos, replacing any photos from a previous
 *  submission of the same (assignment, student) pair. Returns storage paths. */
export async function uploadSubmissionImages(
  assignmentId: string,
  studentId: string,
  images: SubmissionImageUpload[]
) {
  const prefix = `${assignmentId}/${studentId}`;
  // Clear out the previous attempt so storage doesn't accumulate orphans.
  const { data: existing } = await supabase.storage
    .from(WORK_BUCKET)
    .list(prefix);
  if (existing?.length) {
    await supabase.storage
      .from(WORK_BUCKET)
      .remove(existing.map((f) => `${prefix}/${f.name}`));
  }

  const stamp = Date.now();
  const paths: string[] = [];
  for (let i = 0; i < images.length; i++) {
    const img = images[i];
    const ext = EXT_BY_MIME[img.mediaType];
    if (!ext) throw new Error(`Unsupported image type: ${img.mediaType}`);
    const path = `${prefix}/${stamp}-${i}.${ext}`;
    const { error } = await supabase.storage
      .from(WORK_BUCKET)
      .upload(path, Buffer.from(img.data, "base64"), {
        contentType: img.mediaType,
        upsert: true,
      });
    if (error) throw error;
    paths.push(path);
  }
  return paths;
}

/** Short-lived signed URLs for the teacher view. Returns a path→url map. */
export async function signWorkImages(paths: string[]) {
  if (paths.length === 0) return new Map<string, string>();
  const { data, error } = await supabase.storage
    .from(WORK_BUCKET)
    .createSignedUrls(paths, 3600);
  if (error) throw error;
  const map = new Map<string, string>();
  (data ?? []).forEach((d) => {
    if (d.path && d.signedUrl) map.set(d.path, d.signedUrl);
  });
  return map;
}

/** Download a stored work photo as base64 for the vision grading call. */
export async function downloadWorkImageB64(path: string) {
  const { data, error } = await supabase.storage.from(WORK_BUCKET).download(path);
  if (error) throw error;
  const buf = Buffer.from(await data.arrayBuffer());
  const ext = path.split(".").pop() ?? "jpg";
  const mediaType =
    Object.entries(EXT_BY_MIME).find(([, e]) => e === ext)?.[0] ?? "image/jpeg";
  return { mediaType, data: buf.toString("base64") };
}

// ─── Submissions ─────────────────────────────────────────────────────────

export async function listSubmissionsForTeacher(teacherId: string) {
  const { data, error } = await supabase
    .from("educator_submissions")
    .select(`${SUBMISSION_COLS}, educator_assignments!inner(teacher_id)`)
    .eq("educator_assignments.teacher_id", teacherId);
  if (error) throw error;
  const submissions = (data ?? []).map(mapSubmission);

  // One batch signing call across every photo in the class.
  const allPaths = submissions.flatMap((s) => s.images ?? []);
  if (allPaths.length) {
    const signed = await signWorkImages(allPaths);
    submissions.forEach((s) => {
      if (s.images?.length) {
        s.imageUrls = s.images
          .map((p) => signed.get(p))
          .filter((u): u is string => !!u);
      }
    });
  }
  return submissions;
}

