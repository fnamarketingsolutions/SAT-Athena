import { supabase } from "@/lib/supabase/client";
import type { Json } from "@/types/supabase";

/** One question in a structured practice-set assignment (pulled from the
 *  Athena problem bank). `correctIndex`/`explanation` are teacher-only —
 *  strip them before anything student-facing. */
export interface AssignmentQuestion {
  id: string;
  prompt: string;
  options: string[];
  correctIndex: number;
  explanation: string | null;
}

/** Student-safe projection of AssignmentQuestion. */
export interface PublicAssignmentQuestion {
  id: string;
  prompt: string;
  options: string[];
}

/** One question in a free-response quiz (generated by "Write with AI").
 *  `answer` is the teacher-only expected solution/rubric — strip it before
 *  anything student-facing. Discriminated from multiple-choice practice-set
 *  questions by `kind: "free"`. */
export interface FreeResponseQuestion {
  id: string;
  kind: "free";
  prompt: string;
  answer: string;
}

/** Student-safe projection of FreeResponseQuestion. */
export interface PublicFreeResponseQuestion {
  id: string;
  kind: "free";
  prompt: string;
}

/** An assignment's `questions` jsonb is either a multiple-choice practice set
 *  or a free-response quiz — never a mix. */
export type QuizQuestion = AssignmentQuestion | FreeResponseQuestion;
export type PublicQuizQuestion =
  | PublicAssignmentQuestion
  | PublicFreeResponseQuestion;

/** A class/section. `class_id` FKs on students/assignments are nullable
 *  (ON DELETE SET NULL): deleting a class leaves its work "unassigned". */
export interface EducatorClass {
  id: string;
  name: string;
  createdAt: string;
}

export interface EducatorAssignment {
  id: string;
  title: string;
  instructions: string;
  /** Teacher-only. Never returned by the public assignment endpoint. */
  answerKey: string | null;
  /** Multiple-choice practice set, free-response quiz, or null (text homework). */
  questions: QuizQuestion[] | null;
  /** Owning class, or null when unassigned. */
  classId: string | null;
  assignedDate: string; // YYYY-MM-DD — the day the work is handed out
  dueDate: string; // YYYY-MM-DD
  source: string;
  prompt: string | null;
  createdAt: string;
}

export interface EducatorStudent {
  id: string;
  name: string;
  studentEmail: string;
  parentEmail: string;
  /** Owning class, or null when unassigned. */
  classId: string | null;
  createdAt: string;
}

export interface EducatorSubmission {
  id: string;
  assignmentId: string;
  studentId: string;
  response: string | null;
  /** Chosen option index per question (practice-set submissions). */
  answers: number[] | null;
  /** Storage paths of student work photos (educator-work bucket). */
  images: string[] | null;
  /** Short-lived signed URLs for the teacher view; populated by
   *  listSubmissionsForTeacher only. */
  imageUrls?: string[];
  grade: number | null;
  /** The AI grader's feedback. */
  feedback: string | null;
  /** The teacher's own feedback, kept separate from the AI's. */
  teacherFeedback: string | null;
  /** True when the response was AI-invented via the explicit demo action. */
  simulated: boolean;
  submittedAt: string | null;
  gradedAt: string | null;
}

export interface ParentReport {
  id: string;
  studentId: string;
  periodStart: string;
  periodEnd: string;
  summary: string | null;
  sentAt: string;
}

const ASSIGNMENT_COLS =
  "id, title, instructions, answer_key, questions, class_id, assigned_date, due_date, source, prompt, created_at";
const STUDENT_COLS =
  "id, name, student_email, parent_email, class_id, created_at";
const SUBMISSION_COLS =
  "id, assignment_id, student_id, response, answers, images, grade, feedback, teacher_feedback, simulated, submitted_at, graded_at";

const WORK_BUCKET = "educator-work";

