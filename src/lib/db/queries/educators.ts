/** Student-facing fetch by share link — no teacher scoping, public fields
 *  only. The answer key is never selected and practice-set questions are
 *  stripped to their student-safe projection. */
export async function getPublicAssignment(id: string) {
  const { data, error } = await supabase
    .from("educator_assignments")
    .select("id, title, instructions, due_date, questions")
    .eq("id", id)
    .maybeSingle();
  if (error) throw error;
  if (!data) return null;
  const questions =
    (data.questions as unknown as QuizQuestion[] | null) ?? null;
  // Strip teacher-only fields per question kind: `answer` for free-response,
  // `correctIndex`/`explanation` for multiple-choice.
  const publicQuestions: PublicQuizQuestion[] | null = questions
    ? questions.map((q) =>
        "kind" in q && q.kind === "free"
          ? { id: q.id, kind: "free", prompt: q.prompt }
          : {
              id: q.id,
              prompt: q.prompt,
              options: (q as AssignmentQuestion).options,
            }
      )
    : null;
  return {
    id: data.id,
    title: data.title,
    instructions: data.instructions,
    dueDate: data.due_date,
    questions: publicQuestions,
  };
}

