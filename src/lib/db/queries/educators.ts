/** Explicit demo action: store an AI-invented response + grade, marked
 *  simulated so it is badged and excluded from real statistics. Never
 *  overwrites a real submission. */
export async function applySimulatedSubmission(data: {
  assignmentId: string;
  studentId: string;
  response: string;
  grade: number | null;
  feedback: string;
}) {
  const existing = await findSubmission(data.assignmentId, data.studentId);
  if (existing && !existing.simulated) {
    throw new Error("Student already has a real submission");
  }
  const now = new Date().toISOString();
  const { data: row, error } = await supabase
    .from("educator_submissions")
    .upsert(
      {
        ...(existing ? { id: existing.id } : {}),
        assignment_id: data.assignmentId,
        student_id: data.studentId,
        response: data.response,
        answers: null,
        images: null,
        grade: data.grade,
        feedback: data.feedback,
        simulated: true,
        submitted_at: existing?.submittedAt ?? now,
        graded_at: now,
      },
      { onConflict: "assignment_id,student_id" }
    )
    .select(SUBMISSION_COLS)
    .single();
  if (error) throw error;
  return mapSubmission(row);
}

/** Manual teacher edit: grade + the teacher's own feedback change. The AI
 *  feedback (`feedback` column), the response, and the simulated flag are all
 *  preserved. */
export async function saveSubmissionEdit(data: {
  assignmentId: string;
  studentId: string;
  grade: number | null;
  teacherFeedback: string;
}) {
  const existing = await findSubmission(data.assignmentId, data.studentId);
  const now = new Date().toISOString();
  if (existing) {
    const { data: row, error } = await supabase
      .from("educator_submissions")
      .update({
        grade: data.grade,
        teacher_feedback: data.teacherFeedback,
        graded_at: now,
      })
      .eq("id", existing.id)
      .select(SUBMISSION_COLS)
      .single();
    if (error) throw error;
    return mapSubmission(row);
  }
  const { data: row, error } = await supabase
    .from("educator_submissions")
    .insert({
      assignment_id: data.assignmentId,
      student_id: data.studentId,
      grade: data.grade,
      teacher_feedback: data.teacherFeedback,
      simulated: false,
      submitted_at: now,
      graded_at: now,
    })
    .select(SUBMISSION_COLS)
    .single();
  if (error) throw error;
  return mapSubmission(row);
}

